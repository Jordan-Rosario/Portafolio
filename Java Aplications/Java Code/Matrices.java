public class Matrices{
	public static void main(String args[]) {

		int matriz [][] = new int [2][2];

		matriz [0][0] = 15;
		matriz [0][1] = 25;
		matriz [1][0] = 50;
		matriz [1][1] = 30;

		System.out.print("");
		System.out.print("[" + matriz [0][0] + "]");
		System.out.println("[" + matriz [0][1] + "]");
		System.out.print("[" + matriz [1][0] + "]");
		System.out.print("[" + matriz [1][1] + "]");
		System.out.print("");
	}
}